import axios from 'axios'
export default {
    data() {
        return {
            prod: "",
            test: "",
            min: 0,
            max: 99999999,
        }
    },
    methods: {
        getProd() {
            const id = this.$route.params.id;
            axios.get(`/api/product/${id}`).then(res => {
                this.prod = res.data;
            })
        },
        searchProd() {
            let min = document.getElementById('min').value;
            let max = document.getElementById('max').value;
            if (min.length == 0) {
                this.min = 0;
            } else {
                this.min = min;
            }
            if (max.length == 0) {
                this.max = 99999999;
            } else {
                this.max = max;
            }
            // const id = this.$route.params.id;
